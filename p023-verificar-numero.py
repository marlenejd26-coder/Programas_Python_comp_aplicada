print("verificando si un numero es positivo, negativo o cero")

#Entrada
print("dame un numero entero")
numero=int(input())

#Proceso
if numero > 0:
	print("El numero es POSITIVO ")

if numero < 0:
	print ("El numero es NEGATIVO ")

if numero == 0:
	print ("El numero es CERO ")

